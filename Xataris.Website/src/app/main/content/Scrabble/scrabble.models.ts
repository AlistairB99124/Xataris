export interface SimpleResult{
    isSuccess: boolean;
    error: string;
    id: number;
}

export interface Tile {
    type: Type;
    height: number;
    width: number;
    value: string;
    colour: string;
    text: any;
    displayValue: string;
    isNewAnswer: boolean;
}

export enum Type {
    None = 0,
    TripleWord = 1,
    DoubleWord = 2,
    DoubleLetter = 3,
    TripleLetter = 4,
    Start = 5
}

export interface Block{
    id: string;
    letter: string;
    value: number;
    wild: boolean;
    used: boolean;
    isSelected: boolean;
}

export interface Player{
    userId: string;
    tray: any;
    points: number;
    gameId: number;
    id: number;
}

